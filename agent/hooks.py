"""Hooks for CLI browser automation benchmarks.

Provides:
- PreToolUse hook: Hard enforcement of CLI tool isolation via command validation.
- PostToolUse hook: Step tracking, screenshot collection, and signal detection.
- StepTracker: Mutable accumulator for execution steps during a single agent run.

Screenshot Architecture
-----------------------
Auto-screenshots are captured **synchronously** (awaited) after interesting
commands (navigate, click, fill, ...).  Each screenshot gets a deterministic
file path generated by :func:`StepTracker.next_screenshot_path`.

The previous implementation had three problems:
1. Fire-and-forget async screenshot — the file wasn't written yet when the
   directory was scanned, causing missed screenshots.
2. Fragile directory scanning + regex extraction — brittle, race-prone,
   and coupled to CLI output format.
3. No per-CLI validation — screenshot commands were templates with {dir}/{ts}
   placeholders that didn't match actual CLI APIs.

The new design:
- Screenshot command templates use ``{path}`` for a deterministic output path.
- ``_auto_screenshot`` awaits completion before returning the path.
- ``StepTracker`` records screenshots directly from returned paths, with a
  fallback directory scan for edge cases (e.g. agent-initiated screenshots).
"""

from __future__ import annotations

import asyncio
import re
import subprocess
import sys
import time
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

from agent.cli_registry import CliTool


# ---------------------------------------------------------------------------
# Portable async subprocess helper
# ---------------------------------------------------------------------------

async def _run_shell(
    cmd: str, timeout: float = 15
) -> tuple[int, bytes, bytes]:
    """Run a shell command asynchronously and return (returncode, stdout, stderr).

    On Windows, ``asyncio.create_subprocess_shell`` can hang indefinitely
    with some CLI tools (e.g. agent-browser) due to ProactorEventLoop issues.
    We work around this by using ``subprocess.Popen`` in a thread pool on
    Windows, with manual timeout + process tree killing.

    Raises ``asyncio.TimeoutError`` if the command exceeds ``timeout`` seconds.
    """
    if sys.platform == "win32":
        def _run() -> tuple[int, bytes, bytes]:
            import signal
            proc = subprocess.Popen(
                cmd,
                shell=True,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                stdin=subprocess.DEVNULL,
            )
            try:
                stdout, stderr = proc.communicate(timeout=timeout)
                return proc.returncode, stdout, stderr
            except subprocess.TimeoutExpired:
                # Kill entire process tree on Windows
                subprocess.run(
                    f"taskkill /F /T /PID {proc.pid}",
                    shell=True, capture_output=True,
                )
                try:
                    proc.kill()
                except OSError:
                    pass
                return -1, b"", b"TIMEOUT"

        return await asyncio.to_thread(_run)
    else:
        proc = await asyncio.create_subprocess_shell(
            cmd,
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.PIPE,
            stdin=subprocess.DEVNULL,
        )
        stdout, stderr = await asyncio.wait_for(
            proc.communicate(), timeout=timeout
        )
        return proc.returncode, stdout, stderr


# ---------------------------------------------------------------------------
# Always-allowed utility commands (needed for output and basic file ops)
# ---------------------------------------------------------------------------

ALWAYS_ALLOWED_COMMANDS: frozenset[str] = frozenset({
    "echo",   # Completion protocol: echo "TASK_COMPLETE: ..."
    "cat",    # Reading files
    "ls",     # Listing directories
    "pwd",    # Checking working directory
    "mkdir",  # Creating directories (for screenshots)
})

# Max characters to keep in step output for the judge trace.
# Must be large enough for JS eval results to be useful.
STEP_OUTPUT_MAX_CHARS = 8000
CAPTCHA_PATTERNS = re.compile(
    r"captcha|recaptcha|hcaptcha|cloudflare|verify.+human|"
    r"bot.+detection|anti.?bot|turnstile|blocked.+by.+protection|"
    r"access.+denied|challenge.+page",
    re.IGNORECASE,
)

TASK_COMPLETE_PATTERN = re.compile(r"TASK_COMPLETE:\s*(.+)", re.DOTALL)
TASK_IMPOSSIBLE_PATTERN = re.compile(r"TASK_IMPOSSIBLE:\s*(.+)", re.DOTALL)

# Commands that should trigger an auto-screenshot after execution.
# These are the "interesting" moments: navigation, clicks, form submits.
AUTO_SCREENSHOT_KEYWORDS = re.compile(
    r"\b(open|goto|navigate|click|dblclick|submit|fill|select|check|uncheck|press)\b",
    re.IGNORECASE,
)

# Timeout for a single auto-screenshot subprocess call (seconds).
_SCREENSHOT_TIMEOUT_S = 15


# ---------------------------------------------------------------------------
# Step tracking
# ---------------------------------------------------------------------------

@dataclass
class StepTracker:
    """Mutable accumulator for execution steps. Lives for one agent run.

    Key Invariant
    -------------
    ``screenshots`` is an ordered list of **verified** file paths — every
    entry is guaranteed to point to a file that existed at the time it was
    added (via :pymethod:`_add_screenshot`).
    """

    steps: list[dict[str, Any]] = field(default_factory=list)
    screenshots: list[str] = field(default_factory=list)
    captcha_detected: bool = False
    task_impossible: bool = False
    final_result: str | None = None

    _step_counter: int = field(default=0, init=False, repr=False)
    _auto_screenshot_counter: int = field(default=0, init=False, repr=False)
    _known_screenshots: set[str] = field(default_factory=set, init=False, repr=False)
    _screenshot_dir: Path | None = field(default=None, init=False, repr=False)

    # ---- configuration ----

    def set_screenshot_dir(self, screenshot_dir: Path) -> None:
        """Set the primary directory where auto-screenshots are saved."""
        self._screenshot_dir = screenshot_dir

    def snapshot_existing_files(self) -> None:
        """Mark all currently existing screenshot files as 'already known'.

        Call this BEFORE the task starts so that only screenshots created
        during the task run are collected. Without this, old screenshots
        from previous runs in the same directory would be picked up.
        """
        if self._screenshot_dir is None or not self._screenshot_dir.exists():
            return
        for pattern in ("*.png", "*.jpg", "*.jpeg"):
            for f in self._screenshot_dir.glob(pattern):
                self._known_screenshots.add(str(f))

    # ---- screenshot path generation ----

    def next_screenshot_path(self) -> Path:
        """Generate the next deterministic screenshot file path.

        Returns a path like ``<screenshot_dir>/auto_001.png``.
        The caller is responsible for ensuring the directory exists.
        """
        self._auto_screenshot_counter += 1
        assert self._screenshot_dir is not None, "screenshot_dir not set"
        return self._screenshot_dir / f"auto_{self._auto_screenshot_counter:04d}.png"

    # ---- recording ----

    def record_step(
        self,
        command: str,
        output: str,
        is_error: bool = False,
        new_screenshots: list[str] | None = None,
        timestamp: float | None = None,
    ) -> dict[str, Any]:
        """Record a single execution step.

        Args:
            command: The shell command that was executed.
            output: Combined stdout+stderr (truncated to STEP_OUTPUT_MAX_CHARS).
            is_error: Whether the command exited with a non-zero status.
            new_screenshots: Paths to screenshots captured during this step.
                These are already recorded in ``self.screenshots``; we just
                attach them to the step dict for per-step traceability.
            timestamp: Unix timestamp (defaults to ``time.time()``).
        """
        self._step_counter += 1
        ts = timestamp or time.time()

        step = {
            "step": self._step_counter,
            "command": command,
            "output": output[:STEP_OUTPUT_MAX_CHARS],
            "is_error": is_error,
            "screenshots": new_screenshots or [],
            "timestamp": ts,
        }
        self.steps.append(step)
        return step

    # ---- screenshot collection ----

    def _add_screenshot(self, path_str: str) -> bool:
        """Add a screenshot path if it's new and the file exists.

        Returns True if the screenshot was actually added.
        """
        if path_str in self._known_screenshots:
            return False
        if not Path(path_str).exists():
            return False
        self._known_screenshots.add(path_str)
        self.screenshots.append(path_str)
        return True

    def scan_new_screenshots(self) -> list[str]:
        """Find screenshot files in screenshot_dir that aren't already tracked.

        This is the **fallback** collection mechanism.  The primary mechanism
        is ``_auto_screenshot`` which directly records the deterministic path.
        This scan catches screenshots created by the agent's own explicit
        screenshot commands (which we don't control the output path for).
        """
        if self._screenshot_dir is None or not self._screenshot_dir.exists():
            return []

        new_files: list[str] = []
        for pattern in ("*.png", "*.jpg", "*.jpeg"):
            for f in sorted(self._screenshot_dir.glob(pattern)):
                path_str = str(f)
                if self._add_screenshot(path_str):
                    new_files.append(path_str)
        return new_files

    def track_screenshot_from_output(self, output: str) -> list[str]:
        """Extract screenshot file paths mentioned in command output.

        CLI tools often print the path of saved screenshots in their output.
        This captures those paths even when they're outside our screenshot_dir.
        """
        new_files: list[str] = []
        # Common patterns: "Saved screenshot to /path/to/file.png"
        # "Screenshot saved: C:\\Users\\...\\file.png"
        # or just a bare path ending in .png/.jpg
        for match in re.finditer(
            r'(?:saved?\s+(?:screenshot\s+)?(?:to|at|:)\s*)?'
            r'([A-Za-z]:[/\\][^\s"\']+\.(?:png|jpg|jpeg)|'
            r'/[^\s"\']+\.(?:png|jpg|jpeg))',
            output,
            re.IGNORECASE,
        ):
            path_str = match.group(1)
            if self._add_screenshot(path_str):
                new_files.append(path_str)
        return new_files

    def to_trace(self) -> list[dict[str, Any]]:
        """Convert steps to the trace format expected by judge.py."""
        return list(self.steps)


# ---------------------------------------------------------------------------
# PreToolUse hook -- command validation (hard enforcement)
# ---------------------------------------------------------------------------

def _deny(reason: str) -> dict[str, Any]:
    """Build a hook response that denies the tool use."""
    return {
        "hookSpecificOutput": {
            "hookEventName": "PreToolUse",
            "permissionDecision": "deny",
            "permissionDecisionReason": reason,
        }
    }


def _extract_first_command(command: str) -> str:
    """Extract the first command/binary name from a shell command string.

    Handles:
    - Simple commands: "browser-use goto https://..."
    - Env var prefixes: "VAR=1 browser-use click #btn"
    - Pipes: "browser-use screenshot | cat"
    - Chained commands: "browser-use click e1 && echo done"
    - Paths: "/usr/bin/browser-use goto ..."

    Returns the binary name without path.
    """
    # Split on shell operators to get the first command segment
    # Handle &&, ||, ;, | as command separators
    first_segment = re.split(r"\s*(?:&&|\|\||;|\|)\s*", command, maxsplit=1)[0].strip()

    tokens = first_segment.split()
    for token in tokens:
        # Skip env var assignments (KEY=VALUE)
        if "=" in token and not token.startswith("-"):
            continue
        # Return the binary name (strip path)
        return Path(token).name
    return tokens[0] if tokens else ""


def create_pre_tool_use_hook(cli_tool: CliTool):
    """Create a PreToolUse hook that validates Bash commands against the allowed CLI.

    This is the HARD enforcement layer. Combined with system prompt constraints,
    it provides dual-layer tool isolation for benchmark fairness.

    Args:
        cli_tool: The CLI tool definition -- only commands matching its
            ``allowed_prefixes`` (plus ``ALWAYS_ALLOWED_COMMANDS``) are permitted.

    Returns:
        An async hook function compatible with the Agent SDK's hook system.
    """

    async def pre_tool_use_hook(
        input_data: dict[str, Any],
        tool_use_id: str,
        context: dict[str, Any],
    ) -> dict[str, Any]:
        tool_name = input_data.get("tool_name", "")

        # Only validate Bash commands -- let the SDK's allowed_tools
        # handle other tool restrictions. Some internal Claude Code tools
        # (like Skill) may bypass allowed_tools and must not be blocked
        # by this hook.
        if tool_name != "Bash":
            return {}  # Pass through -- not our responsibility

        command = input_data.get("tool_input", {}).get("command", "").strip()
        if not command:
            return _deny("Empty command is not allowed.")

        first_cmd = _extract_first_command(command)

        # Allow target CLI
        if first_cmd in cli_tool.allowed_prefixes:
            return {}  # Approved

        # Allow basic utilities
        if first_cmd in ALWAYS_ALLOWED_COMMANDS:
            return {}  # Approved

        # Deny everything else
        return _deny(
            f"Command '{first_cmd}' is not allowed. "
            f"You may only use: {', '.join(sorted(cli_tool.allowed_prefixes))} "
            f"(and utilities: {', '.join(sorted(ALWAYS_ALLOWED_COMMANDS))})"
        )

    return pre_tool_use_hook


# ---------------------------------------------------------------------------
# PostToolUse hook -- step tracking and signal detection
# ---------------------------------------------------------------------------

def create_post_tool_use_hook(
    cli_tool: CliTool,
    tracker: StepTracker,
    screenshot_dir: Path | None = None,
):
    """Create a PostToolUse hook for tracking execution steps.

    Records every Bash command execution, collects screenshots,
    detects CAPTCHA / completion / impossible-task signals, and
    auto-captures a screenshot after navigation/click commands.

    Args:
        cli_tool: The CLI tool definition (for screenshot command template).
        tracker: The StepTracker accumulator for this agent run.
        screenshot_dir: Directory to save auto-screenshots.

    Returns:
        An async hook function compatible with the Agent SDK's hook system.
    """

    async def post_tool_use_hook(
        input_data: dict[str, Any],
        tool_use_id: str,
        context: dict[str, Any],
    ) -> dict[str, Any]:
        tool_name = input_data.get("tool_name", "")
        if tool_name != "Bash":
            return {}

        command = input_data.get("tool_input", {}).get("command", "")
        # tool_output may be in input_data or context depending on SDK version
        tool_output = input_data.get("tool_output", {})
        output = ""
        is_error = False

        if isinstance(tool_output, dict):
            output = tool_output.get("stdout", "") + tool_output.get("stderr", "")
            is_error = tool_output.get("exit_code", 0) != 0
        elif isinstance(tool_output, str):
            output = tool_output

        # --- Screenshot collection (3 channels) ---
        step_screenshots: list[str] = []

        # Channel 1: Auto-screenshot after interesting commands (synchronous)
        if (
            not is_error
            and AUTO_SCREENSHOT_KEYWORDS.search(command)
            and "screenshot" not in command.lower()
        ):
            auto_path = await _auto_screenshot(cli_tool, tracker, screenshot_dir)
            if auto_path:
                step_screenshots.append(auto_path)

        # Channel 2: Scan screenshot_dir for files created by agent's own
        # screenshot commands (paths we don't control).
        scan_results = tracker.scan_new_screenshots()
        step_screenshots.extend(scan_results)

        # Channel 3: Extract screenshot paths from command output text.
        output_results = tracker.track_screenshot_from_output(output)
        step_screenshots.extend(output_results)

        # Record the step with attached screenshots
        tracker.record_step(
            command=command,
            output=output,
            is_error=is_error,
            new_screenshots=step_screenshots,
        )

        # --- Signal detection ---

        # Detect CAPTCHA in output
        if CAPTCHA_PATTERNS.search(output):
            tracker.captcha_detected = True

        # Detect completion signals
        complete_match = TASK_COMPLETE_PATTERN.search(output)
        if complete_match:
            tracker.final_result = complete_match.group(1).strip()[:5000]

        impossible_match = TASK_IMPOSSIBLE_PATTERN.search(output)
        if impossible_match:
            tracker.task_impossible = True
            if tracker.final_result is None:
                tracker.final_result = impossible_match.group(1).strip()[:5000]

        return {}

    return post_tool_use_hook


# ---------------------------------------------------------------------------
# Auto-screenshot helper
# ---------------------------------------------------------------------------

async def _auto_screenshot(
    cli_tool: CliTool,
    tracker: StepTracker,
    screenshot_dir: Path | None,
) -> str | None:
    """Capture a screenshot after an interesting command.

    Uses the CLI tool's ``screenshot_command`` template from the registry.
    The command is **awaited** (synchronous from the hook's perspective) so
    that the file is guaranteed to exist when we return its path.

    Returns:
        The absolute path to the saved screenshot file, or None on failure.
    """
    if not cli_tool.screenshot_command:
        return None

    if screenshot_dir is None:
        return None

    # Ensure screenshot directory exists
    screenshot_dir.mkdir(parents=True, exist_ok=True)

    # Generate deterministic output path
    out_path = tracker.next_screenshot_path()
    dir_str = str(screenshot_dir)
    path_str = str(out_path)

    # Build command from template
    cmd = cli_tool.screenshot_command.format(path=path_str, dir=dir_str)

    try:
        returncode, stdout_bytes, stderr_bytes = await _run_shell(
            cmd, timeout=_SCREENSHOT_TIMEOUT_S
        )

        stdout_text = stdout_bytes.decode(errors="replace").strip()
        stderr_text = stderr_bytes.decode(errors="replace").strip()

        # Some CLIs (playwright-cli) report errors in stdout with exit=0.
        # Detect this by looking for "### Error" in the output.
        has_cli_error = "### Error" in stdout_text or "Error:" in stderr_text

        if returncode != 0 or has_cli_error:
            print(
                f"[auto-screenshot] FAILED (exit={returncode}): {cmd}",
                flush=True,
            )
            if stderr_text:
                print(f"[auto-screenshot]   stderr: {stderr_text[:300]}", flush=True)
            if has_cli_error and stdout_text:
                print(f"[auto-screenshot]   stdout: {stdout_text[:300]}", flush=True)
            return None

        # Verify the file was actually created
        if out_path.exists():
            path_result = str(out_path)
            tracker._add_screenshot(path_result)
            print(f"[auto-screenshot] OK: {path_result}", flush=True)
            return path_result

        # For CLIs that don't respect our path (fallback: scan directory)
        new_files = tracker.scan_new_screenshots()
        if new_files:
            print(
                f"[auto-screenshot] OK (via scan): {new_files[0]}",
                flush=True,
            )
            return new_files[0]

        # Also try extracting from stdout
        extracted = tracker.track_screenshot_from_output(stdout_text)
        if extracted:
            print(
                f"[auto-screenshot] OK (via output): {extracted[0]}",
                flush=True,
            )
            return extracted[0]

        print(f"[auto-screenshot] WARNING: command succeeded but no file found: {cmd}", flush=True)
        return None

    except asyncio.TimeoutError:
        print(f"[auto-screenshot] TIMEOUT ({_SCREENSHOT_TIMEOUT_S}s): {cmd}", flush=True)
        return None
    except Exception as e:
        print(f"[auto-screenshot] ERROR: {cmd} -> {e}", flush=True)
        return None
